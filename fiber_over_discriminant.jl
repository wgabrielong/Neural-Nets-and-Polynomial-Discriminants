using HomotopyContinuation

@var w1_11, w1_12, w1_21, w1_22, b1_1, b1_2, w2_11, w2_12, w2_21, w2_22, b2_1, b2_2

f1 = 0.25 - w1_11^2*w2_11 + w1_21^2*w2_12 - w1_11^2*w2_21 - w1_21^2*w2_22

f2 = 2*w1_11*w1_12*w2_11 + 2*w1_21*w1_22*w2_12 - 2*w1_11*w1_12*w2_21 - 2*w1_21*w1_22*w2_22

f3 = w1_12^2*w2_11 + w1_22^2*w2_12 - w1_12^2*w2_21 - w1_22^2*w2_22

f4 = 2*w1_11*b1_1*w2_11 + 2*w1_21*b1_2*w2_12 - 2*w1_11*b1_1*w2_21 - 2*w1_21*b1_2*w2_22

f5 = 1 + 2*w1_12*b1_1*w2_11 + 2*w1_22*b1_2*w2_12 - 2*w1_12*b1_1*w2_21 - 2*w1_22*b1_2*w2_22

f6 = b1_1^2*w2_11 + b1_2^2*w2_12 - b1_1^2*w2_21 - b1_2^2*w2_22 + b2_1 - b2_2

F = System([f1, f2, f3, f4, f5, f6])

S = numerical_irreducible_decomposition(F)